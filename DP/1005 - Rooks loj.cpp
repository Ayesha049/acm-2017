ll dp(int i, int j,int k)
{
    if(i<k || j<k) return 0;
    else if(k==0) return 1;
    else if(j==1) return i;
    else return i*dp(i-1,j-1,k-1) + dp(i,j-1,k);
}

void solve()
{
    int n,k;
    sii(n,k);
    ll ans= dp(n,n,k);
    cout<<ans<<endl;
}

